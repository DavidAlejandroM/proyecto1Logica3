/**
 * evento que valida la longiud de la hilera y las teclas que se oprimea para 
 * que no se puedan entrar simbolos, y se actualiza la nueva hilera
 * @param evt 
 */
    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        jButton1.setEnabled(true);
        char c = evt.getKeyChar();
        int code = Character.hashCode(c);
        int tamString = jTextField1.getText().length();
        if (tamString < cp.hilera.getLongitud()) 
        {
            if (code > 96) {
                code = code - 32;
            }
            if(code > 64 && code < 91 )
            {
            // System.out.println(c);
                cp.hilera.insertarCartacter(c, tamString);
            //Crear una hilera
            }
            else if(code == 8)
            {
            //desconectar el ultimo nodo
                cp.hilera.insertarCartacter(c, tamString-1);
            }
            else
            {
                evt.consume();
            } 
        }
        else
        {
            evt.consume();
        }
            
        cp.hilera.recorreIzqDer();
        System.out.println("-------------------------");
        

    }//GEN-LAST:event_jTextField1KeyTyped


